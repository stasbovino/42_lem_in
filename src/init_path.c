#include "lem_in.h"

int			init_path(int **path, int rooms)
{
	int i;

	if (!(*path = (int*)malloc(sizeof(int) * (rooms + 2))))
		return (1);
	i = -1;
	while (++i < (rooms + 2))
		(*path)[i] = 0;
	(*path)[0] = 1;
	(*path)[1] = 1;
	return (0);
}
